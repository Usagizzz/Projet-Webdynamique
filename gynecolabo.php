<h3>Analyse de fertilité</h3>

<p>Notre laboratoire prendre en charge le couple pour leur bilan de fertilité.

Nous effectuons les dosages hormonaux, réalisons le dépistage des MST ainsi que le test de hühner. Il consiste à prélever de la glaire cervicale et a pour but de vérifier la bonne interaction entre les spermatozoïdes et la glaire cervicale.</p>

<br>

<h3>Détection d'infection</h3>

<p>Le laboratoire Omnes Santé accueille aussi les patients en leur apportant une prise en charge orientée et spécialisée dans le diagnostic des infections urogénitales et le dépistage des IST : infections sexuellement transmissibles.</p>

<p>D’autres infections génitales, plus fréquentes chez la femme et non apparentées aux IST, peuvent être également induites par des germes. Ces germes peuvent entraîner une infection génitale. Un prélèvement gynécologique, est indispensable pour détecter la présence de telles infections.</p>

D’une manière générale, tout symptôme persistant et inhabituel doit conduire à une consultation médicale.
